export const GRADE_PROMPT = (correct_recipe: string, input_recipe: string) => `
# 입력
- 사용자가 외운 레시피를 입력합니다.
- 레시피 양식은 다음과 같습니다.
  - "블렌더 / 재료1 + 재료2 + ... / 믹스 / 얼음" 형태로 슬래시("/")로 단계 구분
  - 단위(g, ml 등)는 생략 가능
  - 띄어쓰기나 사소한 오타는 허용합니다.
  - 얼음의 중량은 생략해도 무방합니다.
  - "블렌더 3번 버튼"은 "믹스 3번"과 같은 표현으로 인정합니다.
  - 슬래시로 나뉜 단계들의 순서는 맞아야 하지만, 슬래시 안 재료 순서는 바뀌어도 됩니다.
  - 경우에 따라 슬래시로 구분된 순서가 바뀌더라도 위상적으로 같다면 정답으로 인정합니다.
  - 슬래시로 나뉘어 있지 않은 순서를 슬래시로 나누어 표현해도 무방합니다.

# 루브릭(정답)
${correct_recipe}

# 출력
- 사용자의 레시피가 정답이면: "정답"
- 사용자의 레시피가 오답이면: (사용자 입력에서 틀린 부분을 설명합니다) 

# 추가 지침
- 가능한 한 관대하게 평가하되, 재료가 빠지거나 주요 단계가 누락된 경우는 반드시 오답 처리합니다.
- 재료 이름이 완전히 다를 경우 오답 처리합니다 (ex. 수박 대신 딸기 사용 시).
- "얼음 가득" 표현은 중량 생략 가능, 단 얼음을 전혀 언급하지 않으면 오답 처리합니다.

# 사용자 입력 레시피
${input_recipe}
`;
